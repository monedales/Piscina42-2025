#include <unistd.h>

void	ft_is_negative(int n);

void	ft_is_negative(int n)
{
	char	c;
	char	d;

	if (n >= 0)
	{
		c = 'P';
		write(1, &c, 1);
	}
	else
	{
		d = 'N';
		write(1, &d, 1);
	}
}
